/** \brief Realiza una suma entre dos numeros decimales
 *
 * \param x float El valor del primer operando
 * \param y float El valor del segundo operando
 *
 * \return float Resultado de la suma entre los parametros
 *
 */
float sumar(float x, float y);


/** \brief Realiza una resta entre dos numeros decimales
 *
 * \param x float El valor del primer operando
 * \param y float El valor del segundo operando
 *
 * \return float Resultado de la resta entre los parametros
 *
 */
float restar(float x, float y);


/** \brief Realiza una division entre dos numeros decimales
 *
 * \param x float El valor del dividendo
 * \param y float El valor del divisor
 *
 * \return float Cociente de la division entre los parametros
 *
 */
float dividir(float x, float y);


 /** \brief Realiza una multiplicacion entre dos numeros decimales
 *
 * \param x float El valor del primer operando
 * \param y float El valor del segundo operando
 *
 * \return float Resultado de la multiplicacion entre los parametros
 *
 */
float multiplicar(float x, float y);


/** \brief Realiza el factorial de un numero entero
 *
 * \param x int El valor entero del operando
 *
 * \return int Resultado de la factorizacion del parametro
 *
 */
int factoriar(int x);
